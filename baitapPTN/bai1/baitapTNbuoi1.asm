.ORIG X3000

LDI R0, PHEP_TINH
LDI R1, GIA_TRI_1
LDI R2, GIA_TRI_2

LD R3, PHEP_CONG
ADD R4,R0,R3
BRZ BO_CONG

LD R3, PHEP_TRU
ADD R4,R0,R3
BRZ BO_TRU

LD R3, DICH_TRAI
ADD R4,R0,R3
BRZ BO_DICH_TRAI

;THUC HIEN PHEP OR
NOT R1,R1
NOT R2,R2
AND R0,R1,R2
NOT R0,R0
STI R0, KET_QUA
HALT

BO_CONG	ADD R0,R1,R2
	STI R0,KET_QUA
	HALT
BO_TRU	NOT R2,R2
	ADD R2,R2,#1
	ADD R0,R1,R2
	STI R0, KET_QUA
	HALT
BO_DICH_TRAI	
		AND R0,R0,#0
		LAPLAI	ADD R1,R1,R1
		ADD R2,R2,#-1
		BRP LAPLAI
		STI R1, KET_QUA
		HALT

PHEP_TINH .FILL X2FF0
GIA_TRI_1 .FILL X2FF1
GIA_TRI_2 .FILL X2FF2
KET_QUA .FILL X2FF3

PHEP_CONG .FILL X0
PHEP_TRU .FILL X-1
DICH_TRAI .FILL X-2
OR .FILL X-3
.END